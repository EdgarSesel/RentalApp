    $(document).ready(function() {
        $('#phone_number').on('input', function() {
            var phone_number = $(this).val();
            var phone_error = $('#phone_error');

            if (phone_number.startsWith('+3706') && phone_number.length === 12) {
                phone_error.text('');
            } else {
                phone_error.text('Phone number should start with +3706 and contain exactly 12 symbols');
            }
        });
    });

